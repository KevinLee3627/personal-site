const WIDTH = 32;
const HEIGHT = 32;

// utility function to convert arrays we get from Piskel to a format similar to the artisan bitmaps
// instead of using the colors we get, we convert them into a number so we can sub in
// our colorMap and use whatever colors we like
export function toBitmap(raw: number[]) {
  const seen = new Map<number, number>();
  for (const hex of raw) {
    if (!seen.has(hex)) {
      seen.set(hex, seen.size); // so we increment 0, 1, 2, etc. as we see more colors
    }
  }

  const bitmap = Array.from({ length: HEIGHT }, (_, row) =>
    raw.slice(row * WIDTH, (row + 1) * WIDTH).map((hex) => {
      const hexIndex = seen.get(hex);
      if (hexIndex == null) throw new Error("not supposed to happen :o");
      return hexIndex;
    }),
  );
  return bitmap;
}

